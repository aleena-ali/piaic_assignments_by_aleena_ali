"use strict";
let animals = ["dog", "cat", "rabbit"];
for (let index = 0; index < animals.length; index++) {
    if (animals[index] == "dog") {
        console.log(animals[index] + " is a loyal pet");
    }
    if (animals[index] == "cat") {
        console.log(animals[index] + " is cute");
    }
    if (animals[index] == "rabbit") {
        console.log(animals[index] + " is small");
    }
}
console.log("These animals would make a great pet!");
